                specs.add(cs);
                specs.add(ConnectionSpec.COMPATIBLE_TLS);
                specs.add(ConnectionSpec.CLEARTEXT);

                builder.sslSocketFactory(new Tls12SocketFactory(sc.getSocketFactory()), tm);
